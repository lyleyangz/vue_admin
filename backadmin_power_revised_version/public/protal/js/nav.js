//点击切换nav内容的显示状态
$(".nav").on("click", ".nav-title-icon", function (event) {
  var i = 1;
  var nav = $(this).parent().next();
  var navDisplay = nav.css("display");
  if (navDisplay === "none") {
    nav.slideDown();
    i = 2;
  } else {
    nav.slideUp();
  }
  $(this).css("transform", "rotate(" + i * 180 + "deg)");
});
